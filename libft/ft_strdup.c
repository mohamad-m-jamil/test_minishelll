#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*res;
	size_t	len;
	int		i;

	if (!s)
		return (0);
	len = ft_strlen(s);
	res = (char *)malloc(sizeof(char) * (len + 1));
	if (!res)
		return (NULL);
	i = 0;
	while (s[i])
	{
		res[i] = s[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
